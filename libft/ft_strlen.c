#include "libft.h"

int	ft_strlen(const char *s)
{
	int	i;
	int	f;

	i = 0;
	f = 0;
	if (s == NULL)
		return (0);
	while (s[i] != '\0')
	{
		if (s[i] == '\n')
			f = -1;
		i++;
	}
	return (i - f);
}
